DROP TABLE IF EXISTS LOCATIONS;


CREATE TABLE LOCATIONS(ID NUMERIC PRIMARY KEY,
																								LOCATIONS_DATE DATE, ID_CUSTOMERS NUMERIC,
																							FOREIGN KEY(ID_CUSTOMERS) REFERENCES CUSTOMERS(ID));


